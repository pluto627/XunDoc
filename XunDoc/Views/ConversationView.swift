import SwiftUI
import AVFoundation

struct ConversationView: View {
    let conversation: MedicalConversation
    @Environment(\.dismiss) var dismiss
    @StateObject private var audioPlayer = AudioPlayerManager()
    @State private var showSummary = false
    @State private var selectedMessageId: UUID?
    
    var body: some View {
        NavigationView {
            GeometryReader { geometry in
                HStack(spacing: 0) {
                    // Â∑¶‰æß - ÂØπËØùÂå∫Âüü
                    VStack(spacing: 0) {
                        // Èü≥È¢ëÊí≠ÊîæÊéßÂà∂Ê†è
                        if conversation.audioData != nil && !conversation.audioData!.isEmpty {
                            AudioControlBar(
                                conversation: conversation,
                                audioPlayer: audioPlayer,
                                onSeekToMessage: { message in
                                    selectedMessageId = message.id
                                    audioPlayer.seek(to: message.timestamp)
                                }
                            )
                            .padding(.horizontal, 16)
                            .padding(.vertical, 12)
                            .background(Color.cardBackgroundColor)
                            
                            Divider()
                        } else {
                            // Êó†Èü≥È¢ëÊèêÁ§∫
                            HStack(spacing: 12) {
                                Image(systemName: "info.circle.fill")
                                    .font(.system(size: 16))
                                    .foregroundColor(.orange)
                                
                                Text("Ê≠§ÂØπËØùÊöÇÊó†Èü≥È¢ëÊñá‰ª∂")
                                    .font(.system(size: 14))
                                    .foregroundColor(.textSecondary)
                                
                                Spacer()
                            }
                            .padding(.horizontal, 16)
                            .padding(.vertical, 12)
                            .background(Color.orange.opacity(0.1))
                            
                            Divider()
                        }
                        
                        // ÂØπËØùÂàóË°®
                        if conversation.isTranscribed {
                            ScrollViewReader { proxy in
                                ScrollView(showsIndicators: false) {
                                    LazyVStack(spacing: 16) {
                                        ForEach(conversation.sortedMessages) { message in
                                            MessageBubble(
                                                message: message,
                                                isSelected: selectedMessageId == message.id,
                                                onTap: {
                                                    selectedMessageId = message.id
                                                    audioPlayer.seek(to: message.timestamp)
                                                }
                                            )
                                            .id(message.id)
                                        }
                                    }
                                    .padding(20)
                                }
                                .onChange(of: selectedMessageId) { newId in
                                    if let id = newId {
                                        withAnimation {
                                            proxy.scrollTo(id, anchor: .center)
                                        }
                                    }
                                }
                            }
                        } else {
                            TranscribingView(progress: conversation.transcriptionProgress)
                        }
                    }
                    .frame(width: showSummary ? geometry.size.width * 0.65 : geometry.size.width)
                    
                    // Âè≥‰æß - ÊëòË¶ÅÈù¢Êùø (ÂèØÊäòÂè†)
                    if showSummary {
                        Divider()
                        
                        SummaryPanel(
                            summary: conversation.summary,
                            onClose: {
                                withAnimation(.spring(response: 0.4, dampingFraction: 0.8)) {
                                    showSummary = false
                                }
                            }
                        )
                        .frame(width: geometry.size.width * 0.35)
                        .transition(.move(edge: .trailing))
                    }
                }
            }
            .background(Color.appBackgroundColor)
            .navigationTitle(conversation.title)
            .navigationBarTitleDisplayMode(.inline)
            .toolbar {
                ToolbarItem(placement: .navigationBarLeading) {
                    Button(action: {
                        dismiss()
                    }) {
                        Image(systemName: "xmark")
                            .font(.system(size: 16, weight: .semibold))
                            .foregroundColor(.textSecondary)
                    }
                }
                
                ToolbarItem(placement: .navigationBarTrailing) {
                    Button(action: {
                        withAnimation(.spring(response: 0.4, dampingFraction: 0.8)) {
                            showSummary.toggle()
                        }
                    }) {
                        HStack(spacing: 6) {
                            Image(systemName: showSummary ? "text.alignleft" : "text.alignright")
                                .font(.system(size: 16))
                            Text(showSummary ? "ÈöêËóèÊëòË¶Å" : "ÊòæÁ§∫ÊëòË¶Å")
                                .font(.system(size: 15, weight: .medium))
                        }
                        .foregroundColor(.accentPrimary)
                    }
                }
            }
        }
        .onAppear {
            print("üì± ConversationView onAppear - ÂØπËØù: \(conversation.title)")
            if let audioData = conversation.audioData, !audioData.isEmpty {
                print("üéµ Âä†ËΩΩÈü≥È¢ëÊï∞ÊçÆ: \(audioData.count) Â≠óËäÇ")
                audioPlayer.loadAudio(audioData)
            } else {
                print("‚ö†Ô∏è Êó†Èü≥È¢ëÊï∞ÊçÆÊàñÈü≥È¢ëÊï∞ÊçÆ‰∏∫Á©∫")
            }
        }
        .onDisappear {
            audioPlayer.stop()
        }
    }
}

// MARK: - Èü≥È¢ëÊéßÂà∂Ê†è
struct AudioControlBar: View {
    let conversation: MedicalConversation
    @ObservedObject var audioPlayer: AudioPlayerManager
    let onSeekToMessage: (ConversationMessage) -> Void
    
    var body: some View {
        VStack(spacing: 12) {
            // Êí≠ÊîæÊéßÂà∂
            HStack(spacing: 20) {
                // Êí≠Êîæ/ÊöÇÂÅúÊåâÈíÆ
                Button(action: {
                    audioPlayer.togglePlayPause()
                }) {
                    ZStack {
                        Circle()
                            .fill(Color.accentPrimary)
                            .frame(width: 48, height: 48)
                        
                        Image(systemName: audioPlayer.isPlaying ? "pause.fill" : "play.fill")
                            .font(.system(size: 20))
                            .foregroundColor(.white)
                    }
                }
                
                VStack(alignment: .leading, spacing: 4) {
                    // ËøõÂ∫¶Êù°
                    GeometryReader { geometry in
                        ZStack(alignment: .leading) {
                            RoundedRectangle(cornerRadius: 4)
                                .fill(Color.gray.opacity(0.2))
                                .frame(height: 8)
                            
                            RoundedRectangle(cornerRadius: 4)
                                .fill(Color.accentPrimary)
                                .frame(width: geometry.size.width * audioPlayer.progress, height: 8)
                        }
                    }
                    .frame(height: 8)
                    .gesture(
                        DragGesture(minimumDistance: 0)
                            .onChanged { value in
                                let progress = value.location.x / UIScreen.main.bounds.width
                                audioPlayer.seek(toProgress: min(max(progress, 0), 1))
                            }
                    )
                    
                    // Êó∂Èó¥ÊòæÁ§∫
                    HStack {
                        Text(formatTime(audioPlayer.currentTime))
                            .font(.system(size: 12, weight: .medium, design: .monospaced))
                            .foregroundColor(.textSecondary)
                        
                        Spacer()
                        
                        Text(conversation.formattedDuration)
                            .font(.system(size: 12, weight: .medium, design: .monospaced))
                            .foregroundColor(.textSecondary)
                    }
                }
                
                // Âø´Ëøõ/Âø´ÈÄÄÊåâÈíÆ
                HStack(spacing: 16) {
                    Button(action: {
                        audioPlayer.skip(seconds: -15)
                    }) {
                        Image(systemName: "gobackward.15")
                            .font(.system(size: 20))
                            .foregroundColor(.textPrimary)
                    }
                    
                    Button(action: {
                        audioPlayer.skip(seconds: 15)
                    }) {
                        Image(systemName: "goforward.15")
                            .font(.system(size: 20))
                            .foregroundColor(.textPrimary)
                    }
                }
            }
        }
    }
    
    private func formatTime(_ time: TimeInterval) -> String {
        let minutes = Int(time) / 60
        let seconds = Int(time) % 60
        return String(format: "%02d:%02d", minutes, seconds)
    }
}

// MARK: - Ê∂àÊÅØÊ∞îÊ≥°
struct MessageBubble: View {
    let message: ConversationMessage
    let isSelected: Bool
    let onTap: () -> Void
    
    var isDoctor: Bool {
        message.role == .doctor
    }
    
    var body: some View {
        HStack(alignment: .top, spacing: 12) {
            if isDoctor {
                Spacer(minLength: 60)
            }
            
            VStack(alignment: isDoctor ? .trailing : .leading, spacing: 6) {
                // ËßíËâ≤Ê†áÁ≠æÂíåÊó∂Èó¥Êà≥
                HStack(spacing: 8) {
                    if !isDoctor {
                        Label(message.role.displayName, systemImage: "person.fill")
                            .font(.system(size: 12, weight: .medium))
                            .foregroundColor(.textSecondary)
                        
                        Button(action: onTap) {
                            HStack(spacing: 4) {
                                Image(systemName: "play.circle.fill")
                                    .font(.system(size: 12))
                                Text(message.formattedTimestamp)
                                    .font(.system(size: 11, weight: .medium, design: .monospaced))
                            }
                            .foregroundColor(.accentPrimary)
                        }
                    }
                    
                    if isDoctor {
                        Button(action: onTap) {
                            HStack(spacing: 4) {
                                Text(message.formattedTimestamp)
                                    .font(.system(size: 11, weight: .medium, design: .monospaced))
                                Image(systemName: "play.circle.fill")
                                    .font(.system(size: 12))
                            }
                            .foregroundColor(.accentPrimary)
                        }
                        
                        Label(message.role.displayName, systemImage: "stethoscope")
                            .font(.system(size: 12, weight: .medium))
                            .foregroundColor(.textSecondary)
                    }
                }
                
                // Ê∂àÊÅØÂÜÖÂÆπ
                Text(message.content)
                    .font(.system(size: 15))
                    .foregroundColor(isDoctor ? .white : .textPrimary)
                    .padding(.horizontal, 16)
                    .padding(.vertical, 12)
                    .background(
                        RoundedRectangle(cornerRadius: 18, style: .continuous)
                            .fill(isDoctor ? Color.blue : Color.gray.opacity(0.15))
                    )
                    .overlay(
                        RoundedRectangle(cornerRadius: 18, style: .continuous)
                            .stroke(isSelected ? Color.accentPrimary : Color.clear, lineWidth: 2)
                    )
            }
            
            if !isDoctor {
                Spacer(minLength: 60)
            }
        }
        .animation(.easeInOut(duration: 0.2), value: isSelected)
    }
}

